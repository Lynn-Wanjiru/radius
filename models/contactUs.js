import mongoose from "mongoose";

const contactUsSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
    trim: true,
  },
  lastName: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  description: {
    type: String,
    required: true,
    trim: true,
  },
});

const ContactUs = mongoose.model("ContactUs", contactUsSchema);
export default ContactUs;
